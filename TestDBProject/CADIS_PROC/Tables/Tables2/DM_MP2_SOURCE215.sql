								FROM	"CADIS_PROC"."DM_MP2_SOURCE215_REVISION" 
								GROUP BY "MODEL_CODE","MODEL_DATE","INSTRUMENT_UII"	) RV
						ON RV."MODEL_CODE" = MP."MODEL_CODE" AND RV."MODEL_DATE" = MP."MODEL_DATE" AND RV."INSTRUMENT_UII" = MP."INSTRUMENT_UII"
				WHERE	MP.CADISID <> ISNULL(SR.CADISID,0)
		
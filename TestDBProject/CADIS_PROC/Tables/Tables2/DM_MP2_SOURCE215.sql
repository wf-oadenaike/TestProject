		FOR INSERT,UPDATE
		AS
			INSERT
			INTO	"CADIS_PROC"."DM_MP2_SOURCE215_REVISION"
				(
					"MODEL_CODE","MODEL_DATE","INSTRUMENT_UII"	,
					[REVISION] 	,
					[REVISIONEFFECTIVE],
					[CADISID],
					[CADISROWID]
				)
				SELECT	MP."MODEL_CODE",MP."MODEL_DATE",MP."INSTRUMENT_UII",
					ISNULL(RV.REVISION,0) + 1,
					MP.CADISUPDATED,
					MP.CADISID,
					MP.CADISROWID
				FROM	INSERTED AS MP
					LEFT OUTER JOIN DELETED AS SR
								ON SR."MODEL_CODE" = MP."MODEL_CODE" AND SR."MODEL_DATE" = MP."MODEL_DATE" AND SR."INSTRUMENT_UII" = MP."INSTRUMENT_UII"

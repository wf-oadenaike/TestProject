CREATE VIEW "CADIS"."VW_UI_SEC_QUARTERLY_INCOME"
AS
SELECT
	S.[SECURITY_NAME],
	SI.[FUND_SHORT_NAME],
	SI.[EDM_SEC_ID],
	SI.[FUND_FISCAL_YEAR],
	SI.[DVD_CCY],
	SI.[WITHHOLDING_TAX],
	SI.[DECLARED_DATE],
	SI.[POSITION_DATE],
	SI.[POSITION],
	SI.[MID_PRICE],
	SI.[SPOT_RATE],
	SI.[MARKET_VALUE],
	SI.[Q1_EX_DATE],
	SI.[Q1_RATE],
	SI.[Q1_EARNED_INCOME],
	SI.[Q1_TO_PAY],
	SI.[Q1_NET_INCOME],
	SI.[Q2_EX_DATE],
	SI.[Q2_RATE],
	SI.[Q2_EARNED_INCOME],
	SI.[Q2_TO_PAY],
	SI.[Q2_NET_INCOME],
	SI.[Q3_EX_DATE],
	SI.[Q3_RATE],
	SI.[Q3_EARNED_INCOME],
	SI.[Q3_TO_PAY],
	SI.[Q3_NET_INCOME],
	SI.[Q4_EX_DATE],
	SI.[Q4_RATE],
	SI.[Q4_EARNED_INCOME],
	SI.[Q4_TO_PAY],
	SI.[Q4_NET_INCOME]
FROM "dbo"."T_MASTER_SEC_INCOME" SI
	INNER JOIN "dbo"."T_MASTER_SEC" S ON SI.[EDM_SEC_ID] = S.[EDM_SEC_ID]

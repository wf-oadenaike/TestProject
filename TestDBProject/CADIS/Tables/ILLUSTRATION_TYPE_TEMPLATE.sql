CREATE TABLE [CADIS].[ILLUSTRATION_TYPE_TEMPLATE] (
    [TYPENAME]        NVARCHAR (50)  NOT NULL,
    [FIELDNAME]       NVARCHAR (100) NOT NULL,
    [ILLUSTRATEDNAME] NVARCHAR (100) NOT NULL
);


GO

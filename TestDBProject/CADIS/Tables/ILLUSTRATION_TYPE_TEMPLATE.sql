    ON [CADIS].[ILLUSTRATION_TYPE_TEMPLATE]([TYPENAME] ASC, [FIELDNAME] ASC, [ILLUSTRATEDNAME] ASC);


GO
CREATE NONCLUSTERED INDEX [IDXTYPENAME]
    ON [CADIS].[ILLUSTRATION_TYPE_TEMPLATE]([TYPENAME] ASC);

